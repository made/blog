namespace Made\Blog\Engine\Model;

/**
 * Class PostContent
 *
 * @package Made\Blog\Engine\Model
 */
class PostContent
{
    /**
     * @var string
     */
    private $content;

    /**
     * @return string
     */
    public function getContent(): string
    {
        return $this->content;
    }

    /**
     * @param string $content
     * @return PostContent
     */
    public function setContent(string $content): PostContent
    {
        $this->content = $content;
        return $this;
    }
}
