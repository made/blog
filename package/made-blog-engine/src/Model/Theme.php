namespace Made\Blog\Engine\Model;

/**
 * Class Theme
 *
 * @package Made\Blog\Engine\Model
 */
class Theme
{
    /**
     * @var string
     */
    private $path;

    /**
     * @var string
     */
    private $name;

    /**
     * @var array
     */
    private $configuration;

    /** generated methods */

    /**
     * @return string
     */
    public function getPath(): string
    {
        return $this->path;
    }

    /**
     * @param string $path
     * @return Theme
     */
    public function setPath(string $path): Theme
    {
        $this->path = $path;
        return $this;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return Theme
     */
    public function setName(string $name): Theme
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return array
     */
    public function getConfiguration(): array
    {
        return $this->configuration;
    }

    /**
     * @param array $configuration
     * @return Theme
     */
    public function setConfiguration(array $configuration): Theme
    {
        $this->configuration = $configuration;
        return $this;
    }
}
